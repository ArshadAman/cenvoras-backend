class MultipleLogsTests(AuditServiceSetupMixin, TestCase):
    """Tests verifying that multiple audit log entries can coexist correctly."""

    def test_multiple_actions_create_separate_records(self):
        """Each audit helper call creates a distinct AuditLog row."""
        request = make_request(self.user)
        audit_service.log_create(request, self.employee)
        audit_service.log_update(request, self.employee, before={'status': 'active'}, after={'status': 'inactive'})
        audit_service.log_delete(request, self.employee)
        audit_service.log_download(request, self.employee)
        self.assertEqual(AuditLog.objects.count(), 4)

    def test_action_types_are_distinct(self):
        """All four action types are recorded correctly in a single test run."""
        request = make_request(self.user)
        audit_service.log_create(request, self.employee)
        audit_service.log_update(request, self.employee, before={}, after={})
        audit_service.log_delete(request, self.employee)
        audit_service.log_download(request, self.employee)
        actions = set(AuditLog.objects.values_list('action', flat=True))
        self.assertEqual(actions, {'CREATE', 'UPDATE', 'DELETE', 'DOWNLOAD'})

    def test_different_model_names_stored_correctly(self):
        """model_name overrides are stored independently per log entry."""
        request = make_request(self.user)
        dept = Department.objects.create(tenant=self.tenant, name='Finance')
        audit_service.log_create(request, self.employee, model_name='Employee')
        audit_service.log_create(request, dept, model_name='Department')
        model_names = set(AuditLog.objects.values_list('model_name', flat=True))
        self.assertIn('Employee', model_names)
        self.assertIn('Department', model_names)
